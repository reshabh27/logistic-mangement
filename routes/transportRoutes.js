const express = require('express')
const transportRouter = express.Router()
const transportController = require('../controllers/transportController')
const { auth } = require('../middleware/auth')
transportRouter.get('/testTC', transportController.testTC)
transportRouter.post('/addTransport', auth, transportController.addTransports)
transportRouter.get('/getTransport', auth, transportController.getTranports)
transportRouter.get('/getTransport/:id', auth, transportController.getTransportById)
transportRouter.delete('/deleteTransport/:id', auth, transportController.deleteTransport)
transportRouter.patch('/updateTransport/:id', auth, transportController.updateTransport)
transportRouter.get('/transportQuery', auth, transportController.transportQuery)
module.exports = transportRouter
